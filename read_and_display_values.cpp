#include <bits/stdc++.h> // Includes almost all standard C++ libraries
using namespace std;     // Avoids the need to write 'std::' before standard functions

int main() { 
    int x, y; // Declare two integer variables to store user input

    // Asking the user for two integer values
    cout << "Enter two integer values (x and y): ";
    cin >> x >> y; // Taking input for both x and y from the user

    // Displaying the entered values
    cout << "Value of x: " << x << " and y: " << y << endl;

    // Program executed successfully
    return 0;
}
