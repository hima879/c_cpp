#include <iostream> // Required header for input/output operations
using namespace std; // Allows us to use standard library names without std:: prefix

int main() {
    // Displaying a welcome message to the user
    cout << "Hello World!" << endl; // endl moves the cursor to a new line

    // Printing an additional statement to indicate this is the first C++ program
    cout << "This is my first C++ Program" << endl;

    // Printing a final statement
    cout << "That's all folks!";

    // Returning 0 indicates successful program execution
    return 0;
}
